let x = 5;
let y = 3;

//addition
console.log('x + y = ' , (x + y)) //8

//subtraction
console.log('x - y =' , (x - y)) //2

//multiplication
console.log('x * y =' , (x * y)) //15

//division
console.log('x / y =' ,(x / y)) //1.6666666666666667

//reaminder
console.log('x % y =' , (x % y)) //2

//increment
console.log('++x = ' , ++x); //x is now 6
console.log('x++ = ' , x++);//print 6 and then in increased to 7
console.log('x =' , x); //7

//decrement
console.log('--x =' , --x); //x is now 6
console.log('x-- = ', x--); //print 6 and then in decreased to 5
console.log('x =', x);  //5

//exponentitation
console.log('x ** y =' , x ** y);